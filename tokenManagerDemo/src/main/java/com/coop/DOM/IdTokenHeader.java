package com.coop.DOM;

public class IdTokenHeader {
	 private String alg;
	 private String kid;
	 private String typ;
	 
	public String getAlg() {
		return alg;
	}
	public void setAlg(String alg) {
		this.alg = alg;
	}
	public String getKid() {
		return kid;
	}
	public void setKid(String kid) {
		this.kid = kid;
	}
	public String getTyp() {
		return typ;
	}
	public void setTyp(String typ) {
		this.typ = typ;
	}
	 
	 
	 

}
